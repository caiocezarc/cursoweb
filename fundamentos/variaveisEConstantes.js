var a = 30
let b = 50

console.log(a, b)

// Variáveis com var permite redeclarar a variável.
var a = 100

// Variáveis criadas com let permite apenas ter seu valor alterado.
b = 400

console.log(a, b)

// Variáveis criadas com const não podem ser redeclararadas nem ter seu valor alterado.
const c = 500

console.log(c)
